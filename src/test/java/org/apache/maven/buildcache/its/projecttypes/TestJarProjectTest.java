package org.apache.maven.buildcache.its.projecttypes;

import java.io.IOException;
import java.nio.file.Files;
import java.nio.file.Path;
import java.nio.file.Paths;

import org.apache.maven.buildcache.its.CacheITUtils;
import org.apache.maven.buildcache.its.ReferenceProjectBootstrap;
import org.apache.maven.it.Verifier;
import org.junit.jupiter.api.BeforeAll;
import org.junit.jupiter.api.Test;

/**
 * Verifies that projects that produce a test-JAR (e.g. via
 * {@code maven-jar-plugin:test-jar}) are correctly cached and restored (TC-078, N-03).
 *
 * <p>Uses P06 ({@code p06-dep-edge-cases}) which includes modules that attach test-JARs as
 * secondary artifacts. Build 1 saves all modules to cache. Build 2 hits the cache and
 * verifies that the build completes successfully (all artifacts, including test-JARs,
 * are properly restored).
 */
class TestJarProjectTest {

    @BeforeAll
    static void setUpMaven() throws IOException {
        Path basedir;
        String basedirStr = System.getProperty("maven.basedir");
        if (basedirStr == null) {
            if (Files.exists(Paths.get("target/maven3"))) {
                basedir = Paths.get("target/maven3");
            } else if (Files.exists(Paths.get("target/maven4"))) {
                basedir = Paths.get("target/maven4");
            } else {
                throw new IllegalStateException("Could not find maven home!");
            }
        } else {
            basedir = Paths.get(basedirStr);
        }
        Path mavenHome = Files.list(basedir.toAbsolutePath())
                .filter(p -> Files.exists(p.resolve("bin/mvn")))
                .findAny()
                .orElseThrow(() -> new IllegalStateException("Could not find maven home"));
        System.setProperty("maven.home", mavenHome.toString());
        mavenHome.resolve("bin/mvn").toFile().setExecutable(true);
    }

    @Test
    void testJarArtifactCachedAndRestored() throws Exception {
        Path p06 = Paths.get("src/test/projects/reference-test-projects/p06-dep-edge-cases")
                .toAbsolutePath();
        Verifier verifier = ReferenceProjectBootstrap.prepareProject(p06, "TestJarProjectTest");
        verifier.setAutoclean(false);

        // Build 1: cold cache — all modules including test-JARs saved
        verifier.setLogFileName("../log-1.txt");
        verifier.executeGoal("verify");
        verifier.verifyErrorFreeLog();
        verifier.verifyTextInLog(CacheITUtils.CACHE_SAVED);

        // Build 2: warm cache — hit; test-JARs must be properly restored
        verifier.setLogFileName("../log-2.txt");
        verifier.executeGoal("verify");
        verifier.verifyErrorFreeLog();
        verifier.verifyTextInLog(CacheITUtils.CACHE_HIT);
    }
}
